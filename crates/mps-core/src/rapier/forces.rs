//! Force registry — type-erased force-law registration with per-step dispatch.
//!
//! ## Motivation
//!
//! Before this module, every force subsystem (Newtonian gravity, air drag,
//! electromagnetic, aerodynamic surfaces, …) was applied via hard-coded
//! `if let Some(law)` branches inside `world_step` or its callees.  Adding a
//! new force type meant touching five files: the struct definition, the setter
//! FFI, `CustomPhysicsState`, the `world_step` dispatch, and cbindgen exports.
//!
//! The registry centralises discovery: a force law implements [`ForceLaw`],
//! registers itself into [`ForceRegistry`] (held by `PhysicsWorld`), and
//! `world_step` simply calls `registry.apply_all()`.  A new law is now a
//! single struct plus a `register_*` call — no dispatch logic to update.
//!
//! ## Architecture
//!
//! ```text
//!                  world_step
//!                     │
//!            ForceFacade (bodies + colliders + narrow_phase + frame_log)
//!            ╱    │     ╲
//!    ForceLaw₁  ForceLaw₂  …  force_set_*_law FFI  (all use facade)
//!            ╲    │     ╱
//!            ForceRegistry  →  drain_report()  →  CustomPhysicsReport
//!   ```
//!
//! ## ForceFacade — the unified force application surface
//!
//! [`ForceFacade`] is the sole entry-point for applying forces to bodies.
//! Every `add_force` / `add_torque` call flows through it, so the frame-log
//! automatically records which [`ForceLawType`] produced each force.
//! `ForceReport` is then generated by draining this log — no manual bookkeeping.
//!
//! To add a new force type:
//! 1. Add a variant to [`ForceLawType`].
//! 2. Implement [`ForceLaw`] and register it into [`ForceRegistry`].
//! 3. In the `apply()` implementation, use the `facade` to push forces.
//! The report is collected automatically.

