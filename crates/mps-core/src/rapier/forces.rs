/// A pluggable physics force law.
///
/// Implementors compute per-frame forces and apply them via the [`ForceFacade`].
/// The facade automatically records which [`ForceLawType`] produced each force,
/// so implementors **do not** need to manage `ForceReport` themselves.
///
/// # Implementation notes
///
/// - `apply` receives a `&mut ForceFacade` — use `facade.add_force(handle, force, self.law_type())`.
/// - `law_type()` must return the same value for the lifetime of the law.
/// - `clone_box()` is used when the FFI layer needs to read a law's config.
pub trait ForceLaw: Send + Sync {
    /// The categorisation tag for this law.
    fn law_type(&self) -> ForceLawType;

    /// Whether this law should run this frame.
    fn enabled(&self) -> bool {
        true
    }

    /// Apply the law to all relevant bodies via the facade.
    ///
    /// Use `facade.add_force()` / `facade.add_torque()` to apply forces.
    /// The facade automatically records contributions per [`ForceLawType`].
    fn apply(&self, facade: &mut ForceFacade<'_>);

    /// Heap-clone this law (used for FFI read-back).
    fn clone_box(&self) -> Box<dyn ForceLaw>;
}

